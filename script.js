function renderAll() {
  setTextContent();
  renderMetrics();
  renderWorks();
  renderSections();
}

$("[data-lang-toggle]").addEventListener("click", () => {
  currentLang = currentLang === "zh" ? "en" : "zh";
  renderAll();
});

$(".nav-toggle").addEventListener("click", () => {
  const nav = $(".nav-links");
  const isOpen = nav.classList.toggle("is-open");
  $(".nav-toggle").setAttribute("aria-expanded", String(isOpen));
});

$(".nav-links").addEventListener("click", (event) => {
  if (event.target.tagName === "A") {
    $(".nav-links").classList.remove("is-open");
    $(".nav-toggle").setAttribute("aria-expanded", "false");
  }
});

const dialogCloseButton = $("[data-dialog-close]");
if (dialogCloseButton) {
  dialogCloseButton.addEventListener("click", () => $("#caseDialog").close());
}

const caseDialog = $("#caseDialog");
if (caseDialog) {
  caseDialog.addEventListener("click", (event) => {
    if (event.target.id === "caseDialog") {
      caseDialog.close();
    }
  });
}

