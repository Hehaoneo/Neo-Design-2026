let currentIntroLang = "zh";

const $ = (selector) => document.querySelector(selector);
const $$ = (selector) => Array.from(document.querySelectorAll(selector));

function getProjectById(id) {
  return portfolioProjects.find((project) => project.id === id);
}

function localizeProject(project, key) {
  if (!project) return "";
  if (currentIntroLang === "en") return project[`${key}En`] || project[key];
  return project[key];
}

function setIntroText() {
  const dictionary = introContent[currentIntroLang];
  $$("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) node.textContent = dictionary[key];
  });
  document.documentElement.lang = currentIntroLang === "zh" ? "zh-CN" : "en";
  document.title =
    currentIntroLang === "zh" ? "何浩个人介绍 | 3-5分钟面试自我介绍" : "Hao He Intro | 3-5 Minute Interview Introduction";
  $("[data-lang-toggle]").textContent = currentIntroLang === "zh" ? "EN" : "中";
}

function renderIntroMetrics() {
  $("#introMetrics").innerHTML = introContent[currentIntroLang].metrics
    .map(
      ([value, label]) => `
        <article class="intro-metric-card">
          <strong>${value}</strong>
          <span>${label}</span>
        </article>
      `
    )
    .join("");
}

function renderIntroScript() {
  $("#introScript").innerHTML = introContent[currentIntroLang].script
    .map(
      ({ time, title, text }) => `
        <article class="intro-script-card">
          <span>${time}</span>
          <h3>${title}</h3>
          <p>${text}</p>
        </article>
      `
    )
    .join("");
}

function renderIntroTimeline() {
  $("#introTimeline").innerHTML = introContent[currentIntroLang].timeline
    .map(({ period, company, role, note, detail, logo }) => {
      const noteText = currentIntroLang === "en" ? `(${note})` : `（${note}）`;
      return `
        <article class="timeline-item">
          <span class="timeline-period">${period}</span>
          <span class="timeline-node" aria-hidden="true"></span>
          <div class="timeline-company">
            <span class="company-logo-wrap">
              <img class="company-logo" src="${logo}" alt="${company} logo" loading="lazy" />
            </span>
            <div class="timeline-copy">
              <h3><strong>${company}</strong><span>·</span>${role}<em>${noteText}</em></h3>
              <p>${detail}</p>
            </div>
          </div>
        </article>
      `;
    })
    .join("");
}

function renderIntroLeadership() {
  $("#introLeadership").innerHTML = introContent[currentIntroLang].leadership
    .map(
      ([title, text], index) => `
        <article class="intro-leadership-card">
          <span>${String(index + 1).padStart(2, "0")}</span>
          <h3>${title}</h3>
          <p>${text}</p>
        </article>
      `
    )
    .join("");
}

function renderIntroEvidence() {
  const dictionary = introContent[currentIntroLang];
  $("#introEvidence").innerHTML = dictionary.evidenceGroups
    .map(
      ({ title, text, projectIds }) => `
        <article class="intro-evidence-card">
          <h3>${title}</h3>
          <p>${text}</p>
          <div class="intro-project-links">
            ${projectIds
              .map((id) => {
                const project = getProjectById(id);
                if (!project) return "";
                return `<a href="./project.html?id=${id}">${localizeProject(project, "title")}</a>`;
              })
              .join("")}
          </div>
        </article>
      `
    )
    .join("");
}

function renderIntroMemoryPoints() {
  $("#introMemoryPoints").innerHTML = introContent[currentIntroLang].memoryPoints
    .map(
      (point) => `
        <article class="intro-memory-card">
          <p>${point}</p>
        </article>
      `
    )
    .join("");
}

function renderIntroPage() {
  setIntroText();
  renderIntroMetrics();
  renderIntroScript();
  renderIntroTimeline();
  renderIntroLeadership();
  renderIntroEvidence();
  renderIntroMemoryPoints();
}

$("[data-lang-toggle]").addEventListener("click", () => {
  currentIntroLang = currentIntroLang === "zh" ? "en" : "zh";
  renderIntroPage();
});

$(".nav-toggle").addEventListener("click", () => {
  const nav = $(".nav-links");
  const isOpen = nav.classList.toggle("is-open");
  $(".nav-toggle").setAttribute("aria-expanded", String(isOpen));
});

$(".nav-links").addEventListener("click", (event) => {
  if (event.target.tagName === "A") {
    $(".nav-links").classList.remove("is-open");
    $(".nav-toggle").setAttribute("aria-expanded", "false");
  }
});

renderIntroPage();
